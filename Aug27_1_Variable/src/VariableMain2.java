
public class VariableMain2 {
	public static void main(String[] args) {
		//레스토랑 이름이 김밥천국 저장
		//출력
		String restName = "김밥천국";
		System.out.println(restName);
		
		//레스토랑 크기 100.386평 저장
		//출력
		double restSize = 100.386;
		System.out.println(restSize);
		
		//레스토랑 테이블이 20개 저장
		//출력
		int restTable = 20;
		System.out.println(restTable);
		
		//레스토랑 영업중 저장
		//출력
		boolean restOpen = true;
		System.out.println(restOpen);
		
	}

}
